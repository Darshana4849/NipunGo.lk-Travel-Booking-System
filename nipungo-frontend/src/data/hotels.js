export const getHotelById = (id) =>
  hotels.find((h) => h.id === parseInt(id) || h.slug === id);

export const getFeaturedHotels = () =>
  hotels.filter((h) => h.featured);

export const getHotelsByDestination = (destination) =>
  hotels.filter((h) => h.nearDestination === destination);

export const searchHotels = (query) => {
  const q = query.toLowerCase();
  return hotels.filter(
    (h) =>
      h.name.toLowerCase().includes(q) ||
      h.location.toLowerCase().includes(q) ||
      h.category.toLowerCase().includes(q)
  );
};

